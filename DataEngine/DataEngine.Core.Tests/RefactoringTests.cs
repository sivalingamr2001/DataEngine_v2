    [Theory]
    [InlineData("123", 123L)]
    [InlineData("0", 0L)]
    [InlineData("-45", -45L)]
    public void CoerceValue_Int_ParsesCorrectly(string input, long expected)
    {
        var result = DataTypeConverter.CoerceValue(input, "INT");
        Assert.Equal(expected, result);
    }

    [Theory]
    [InlineData("123.45", 123.45)]
    [InlineData("0.0", 0.0)]
    public void CoerceValue_Decimal_ParsesCorrectly(string input, double expected)
    {
        var result = DataTypeConverter.CoerceValue(input, "DECIMAL");
        Assert.Equal((decimal)expected, result);
    }

    [Theory]
    [InlineData("true", true)]
    [InlineData("1", true)]
    [InlineData("false", false)]
    [InlineData("0", false)]
    public void CoerceValue_Bool_ParsesCorrectly(string input, bool expected)
    {
