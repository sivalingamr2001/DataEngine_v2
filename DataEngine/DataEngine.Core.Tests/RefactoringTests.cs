    [Fact]
    public void CoerceValue_Guid_ParsesCorrectly()
    {
        var target = Guid.NewGuid();
        var result = DataTypeConverter.CoerceValue(target.ToString(), "GUID");
        Assert.Equal(target, result);
    }

    [Fact]
    public void CoerceValue_DateTime_ParsesCorrectly()
    {
        var targetStr = "2026-07-07T12:00:00Z";
        var result = DataTypeConverter.CoerceValue(targetStr, "DATETIME");
        Assert.IsType<DateTime>(result);
        Assert.Equal(DateTime.Parse(targetStr).ToUniversalTime(), ((DateTime)result!).ToUniversalTime());
    }

    [Fact]
    public void CoerceValue_InvalidValue_ReturnsOriginal()
    {
        var result = DataTypeConverter.CoerceValue("not-a-number", "INT");
        Assert.Equal("not-a-number", result);
    }

    [Fact]
    public void GetPrimaryKeyMapper_ResolvesAutoGen()
    {
        var mappers = new List<FieldMapper>
        {
            new() { FieldName = "name", ColumnName = "name", DataType = "string" },
            new() { FieldName = "orderId", ColumnName = "order_id", DataType = "long", Properties = "AutoGenerated" }
        };

        var pk = TransactionService.GetPrimaryKeyMapper(mappers);
        Assert.Equal("orderId", pk.FieldName);
        Assert.Equal("order_id", pk.ColumnName);
    }

