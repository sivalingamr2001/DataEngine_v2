    [Fact]
    public async Task TableNameValidator_BlocksProhibitedSystemTables()
    {
        var opt = Microsoft.Extensions.Options.Options.Create(new DataEngineOptions
        {
            Connections = [ new DatabaseOptions { ConnectionString = "Server=localhost", FieldMappersTableName = "de_field_mappers" } ],
            Audit = new AuditOptions { ReadAuditTableName = "de_audit_read_log", WriteAuditTableName = "de_audit_write_log" }
        });

        var validator = new TableNameValidator(opt);

        await Assert.ThrowsAsync<UnauthorizedAccessException>(() => validator.EnsureAllowedAsync("de_query_definitions"));
        await Assert.ThrowsAsync<UnauthorizedAccessException>(() => validator.EnsureAllowedAsync("de_field_mappers"));
        await Assert.ThrowsAsync<UnauthorizedAccessException>(() => validator.EnsureAllowedAsync("de_audit_read_log"));
        await Assert.ThrowsAsync<UnauthorizedAccessException>(() => validator.EnsureAllowedAsync("de_audit_write_log"));
        
        var exception = await Record.ExceptionAsync(() => validator.EnsureAllowedAsync("users"));
        Assert.Null(exception);
    }

    [Fact]
    public async Task ValidationService_EvaluatesBusinessRules()
    {
        var options = Microsoft.Extensions.Options.Options.Create(new DataEngineOptions());
        var mockCache = new Microsoft.Extensions.Caching.Memory.MemoryCache(new Microsoft.Extensions.Caching.Memory.MemoryCacheOptions());
        var mockLogger = new Microsoft.Extensions.Logging.Abstractions.NullLogger<ValidationService>();

        var validationService = new ValidationService(options, null!, mockCache, mockLogger);

        var config = new ValidationConfiguration
        {
            Fields = [],
            BusinessRules = new List<BusinessRule>
            {
                new()
                {
                    RuleName = "CheckPriceRange",
                    Expression = "price >= cost",
                    ErrorMessage = "Price cannot be less than cost."
                },
                new()
                {
                    RuleName = "CheckStatus",
                    Expression = "status = 'Active'",
                    ErrorMessage = "Status must be Active."
                }
            }
        };

        mockCache.Set("validation::products", config, TimeSpan.FromMinutes(10));

        var passingData = new Dictionary<string, object>
        {
            { "price", 15.50m },
            { "cost", 10.00m },
            { "status", "Active" }
        };

        var resultPass = await validationService.ValidateAsync("products", passingData);
        Assert.True(resultPass.IsValid);

        var failingPriceData = new Dictionary<string, object>
        {
            { "price", 8.50m },
            { "cost", 10.00m },
            { "status", "Active" }
        };

        var resultFailPrice = await validationService.ValidateAsync("products", failingPriceData);
        Assert.False(resultFailPrice.IsValid);
        Assert.Single(resultFailPrice.Errors);
        Assert.Equal("Price cannot be less than cost.", resultFailPrice.Errors[0].ErrorMessage);

        var failingStatusData = new Dictionary<string, object>
        {
            { "price", 15.50m },
            { "cost", 10.00m },
            { "status", "Inactive" }
        };

        var resultFailStatus = await validationService.ValidateAsync("products", failingStatusData);
        Assert.False(resultFailStatus.IsValid);
        Assert.Single(resultFailStatus.Errors);
        Assert.Equal("Status must be Active.", resultFailStatus.Errors[0].ErrorMessage);
    }
